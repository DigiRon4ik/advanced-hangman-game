def print_hangman(part: int, color: str):
    hangman_parts = [
        ' \n  |\n  |\n  |\n  |\n  |\n==+========+==\n',
        ' \n  | /\n  |/\n  |\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /\n  |/\n  |\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/\n  |\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |       |\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |      /|\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |      /|\\\n  |\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |      /|\\\n  |      /\n  |\n==+========+==\n',
        ' =+=======+=\n  | /     |\n  |/      O\n  |      /|\\\n  |      / \\\n  |\n==+========+==\n'
    ]
    print(color + hangman_parts[part])